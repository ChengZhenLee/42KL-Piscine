#include "ft_list.h"

t_list	*ft_list_last(t_list *begin_list)
{
	t_list	*temp;

	temp = begin_list;
	if (!(begin_list))
		return (0);
	while (temp->next != NULL)
		temp = temp->next;
	return (temp);
}
