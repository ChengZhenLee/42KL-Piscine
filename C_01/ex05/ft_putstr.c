#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char	*temp;

	temp = str;
	while (*temp != '\0')
	{
		ft_putchar(*temp);
		temp++;
	}
}
